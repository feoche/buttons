// ── helpers ──────────────────────────────────────────────────────────────────
function matches(terms, title, desc, kw) {
  const haystack = [title, desc, ...kw].join(" ").toLowerCase();
  return terms.some((t) => haystack.includes(t.toLowerCase()));
}

function categorize(item) {
  const t = item.title || "";
  const d = item.description || "";
  const kw = item.keywords || [];
  if (matches(MEME, t, d, kw)) return "meme";
  if (matches(SFX, t, d, kw)) return "sfx";
  if (matches(MUSIQUE, t, d, kw)) return "musique";
  if (matches(GAMING, t, d, kw)) return "gaming";
  if (matches(FILM, t, d, kw)) return "film";
  if (matches(HUMOUR, t, d, kw)) return "humour";
  return "autre";
}

// ── main ─────────────────────────────────────────────────────────────────────
const raw = readFileSync(DATA_PATH, "utf-8");
const data = JSON.parse(raw);

const updated = data.map((item) => {
  const cat = categorize(item);
  const { title, description, video, keywords, category: _oldCat, ...rest } = item;
  const out = { title };
  if (description !== undefined) out.description = description;
  if (video !== undefined) out.video = video;
  if (keywords !== undefined) out.keywords = keywords;
  out.category = cat;
  return { ...out, ...rest };
});

writeFileSync(DATA_PATH, JSON.stringify(updated, null, 2) + "\n", "utf-8");

// Stats
const counts = { gaming: 0, film: 0, humour: 0, meme: 0, musique: 0, sfx: 0, autre: 0 };
for (const item of updated) counts[item.category]++;
console.log(`Done! ${updated.length} entries categorized:`);
console.log(counts);

